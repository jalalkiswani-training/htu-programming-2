package edu.htu.review2;

import java.util.Scanner;

public class DataTypes {
	public static void main(String[] args) {
		/* primitives
		 * 
		 * Datatype variableName[=initialValue];
		 */
		byte b=10; //1 bytes
		short s=20; //2 bytes		
		char c='a';//2 bytes
		int i=50;//4 bytes
		float f=10.34f;//4 bytes 
		long l=40;//8 bytes
		double d=103.958; //8 bytes
		boolean b1=true;//false
		
		/* Based on the concept of (Clarity over Quality) it recommanded to always use  
		 * 1- int for decimal numbers
		 * 2- double for real numbers
		 */
		
		
		/* classes 
		 *  Datatype varName(reference) = new DataType();(construction : call the new command with a method with the same name 
		 *  of the class 
		 */
		Scanner sc=new Scanner(System.in);
	}
}
