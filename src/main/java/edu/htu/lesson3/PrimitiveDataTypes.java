package edu.htu.lesson3;

import java.util.Scanner;

public class PrimitiveDataTypes {
	public static void main(String[] args) {
		//Primitives 
		
		//byte: Data type
		//b: variable name
		//10: initial value
		byte b=10; // 1 byte: 8 bit
		
		//short: data type
		//s: variable name
		//20: initial value
		short s=20;// 2-bytes
		char c='a'; // 2-bytes
		
		int i=50;//4 bytes decimal
		float f=20.2f;//4 bytes & real numbers
		
		long l=100000;//8 bytes
		double d=100.4567;//8bytes & real numbers
		
		boolean b1=true;//false 
		
		//Non primitive data types: classes
		//for example
		Scanner s1=new Scanner(System.in);
		String str="Hi";
	}
}
